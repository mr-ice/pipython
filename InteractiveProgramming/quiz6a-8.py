class BankAccount:
    def __init__(self,initial_balance):
        self._balance = initial_balance
        self._fees = 0

    def deposit(self,amount):
        self._balance += amount

    def withdraw(self,amount):
        if self._balance < amount:
            self._balance -= 5.00
            self._fees += 5.00
        self._balance -= amount

    def get_balance(self):
        return self._balance

    def get_fees(self):
        return self._fees

